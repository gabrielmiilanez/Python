a = int(input("Digite o primeiro numero: "))
b = int(input("Digite o segundo numero: "))

soma = a + b
print(f"A soma entre {a} e {b} é igual a {soma}.")

diferença = a - b
print(f"A diferença entre {a} e {b} é igual a {diferença}.")

produto = a * b
print(f"O produto entre {a} e {b} é igual a {produto}.")

print(soma, diferença, produto)